'''
[문제 요약]
여러 개의 OX문제 중 연속적으로 답을 맞히는 경우 다음과 같이 가산점을 주려고 합니다.

1번 문제가 맞는 경우 1점으로 계산합니다.
앞의 문제에서 답이 틀리다가 답이 맞는 처음 문제는 1점으로 계산합니다.
연속으로 답이 맞은 경우 두번째 문제는 2점, 세번째 문제는 3점의 방식으로 계산합니다.
틀린 문제는 0점으로 계산합니다.

ex) 1 0 1 1 1 0 0 1 1 0 => 1 + 1 + 2 + 3 + 1 + 2 = 10점
'''

N = int(input())
number = list(map(int, input().split()))

pre = 0
sum = 0
for i in range(N):
    if number[i] == 1:
        pre += 1
        sum += pre
    else:
        pre = 0
print(sum)